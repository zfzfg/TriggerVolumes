package de.zfzfg.triggervolumes.models;

/**
 * Represents when an action should be triggered.
 * 
 * @author zfzfg
 */
public enum TriggerType {
    /**
     * Action is triggered when a player enters the volume.
     */
    ENTER,
    
    /**
     * Action is triggered when a player leaves the volume.
     */
    LEAVE
}
